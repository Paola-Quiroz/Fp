#Ejercicio 2: pedir calificaciones e imprima el promedio
prom = 0 ;
cala  =  float ( input ( 'Insterta la primera calificación:' ));
calb  =  float ( input ( 'Insterta la segunda calificación:' ));
calc  =  float ( input ( 'Insterta la tercer calificación:' ));
cald  =  float ( input ( 'Insterta la cuarta calificación:' ));
cale  =  float ( input ( 'Insertar la quinta calificación:' ));
prom = (( cala + calb + calc + cald + cale ) / 5 );
print ( "El promedio es:" , prom);